    func userNotificationCenter(_ center: UNUserNotificationCenter, willPresent notification: UNNotification, withCompletionHandler completionHandler: @escaping (UNNotificationPresentationOptions) -> Void) {
        completionHandler([.banner,.sound]) // you can remove the sound this is inside the app
    }
    // to check which notification the user press on to take it to spacific page
    func userNotificationCenter(_ center: UNUserNotificationCenter, didReceive response: UNNotificationResponse, withCompletionHandler completionHandler: @escaping () -> Void) {
        print("notification clicked ")
        
        // to read what it passed from the userInfo
        if let dictionary = response.notification.request.content.userInfo as? [String: Any] {
             // print the dictionary content
            print("dictionary \(dictionary)")
            //print the name content
            print("name: \(dictionary["name"] ?? "default value")")
        }
        
    }
